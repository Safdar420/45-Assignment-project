if (AlienColor2 === 'green') {
    console.log('Congratulations! You just earned 5 points for shooting down the green alien.');
} else if (AlienColor2 === 'yellow') {
    console.log('No points earned for shooting the yellow alien.');
} else if (AlienColor2 === 'red') {
    console.log('No points earned for shooting the red alien.');
} else {
    console.log('Invalid alien color.');
}

